// variables 3 types and based on scopes we will use and declare a variable

// new method-let(value is changeable)

let username = 'madhava';
console.log(username);
username = 'madhavareddy';
console.log(username);
 
// old method-var (changeable)

var gameName = 'cricket';
console.log(gameName);

gameName = 'hockey';
console.log(gameName);

// const (not changeable and more use in industry)

const userNumber = 99667
// userNumber = 9867 
console.log(userNumber)


